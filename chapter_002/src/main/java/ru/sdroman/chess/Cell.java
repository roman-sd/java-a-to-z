package ru.sdroman.chess;

/**
 * Class Cell.
 *
 * @author sdroman.
 * @version 1.0
 * @since 07.12.2016
 */
public class Cell {

    /**
     * x.
     */
    public int x;
    /**
     * y.
     */
    public int y;

    /**
     * Constructs a new Cell.
     *
     * @param x int
     * @param y int
     */
    public Cell(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
